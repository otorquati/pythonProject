# Exemplo de uso de dado do tipo set e set e uso de For Loops
my_set = {"Janeiro", "Fevereiro", "Março"}
# Adicionando elementos ao tipo de dados set
my_set.add("Abril")
for element in my_set:
    print(element)

# Para remover algum elemento
print("Removendo um elemento")
my_set.remove("Abril")
for element in my_set:
    print(element)

# Não é possivel acessar os elementos do tipo set como é feito no tipo list

